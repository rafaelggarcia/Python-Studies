"""
- Poo é um paradigma de programação que utiliza o mapeamento de objetos do mundo real para modelos computacionais.

- Paradigma de programação é a forma/metodologia utilizada para pensar/desenvolver sistemas.

Principais elementos de POO:
- Classe -> Modelo do objeto do mundo rea representado computacionalmente
- Atributo -> Características do objeto.
- Método -> Comportamento do objeto. (função inclusa dentro da classe).
- Construtor -> Método especial utilizado para criar os objetos.
- Objeto -> Instância da classe. 

"""

numero = 10

print(numero)
print(type(numero))

nome = 'Geek'
print(nome)
print(type(nome))


