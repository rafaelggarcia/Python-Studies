n1 = float  (input('Digite a primeira nota: '))
n2 = float(input('Digite a segunda nota: '))
media = (n1 + n2) / 2
print(f'Sua média é {media}')
if media >= 6:
    print('Você passou de ano!')
else:
    print('Você não passou de ano!')


